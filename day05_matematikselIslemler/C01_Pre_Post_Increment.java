package day05_matematikselIslemler;

public class C01_Pre_Post_Increment {
    public static void main(String[] args) {

        int sayi1= 10;
        int sayi2= sayi1+1; // sayi2 11
        sayi2+=5; // 16


        int sayi3=sayi2++; // sayi 2 değeri, sayi3 e atanacak
                           // sayi2 bir arttırılacak


        int sayi4= ++ sayi1; //sayi1 bir arttırılacak
                            // sayi1 değeri sayi4 e aanacak


    }
}
